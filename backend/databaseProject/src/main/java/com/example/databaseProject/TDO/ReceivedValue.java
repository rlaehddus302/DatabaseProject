package com.example.databaseProject.TDO;

public class ReceivedValue {
	public int [][] freePeriod; 
	double consecutiveClassTime;
	double intervalTime;
	
	public int[][] getFreePeriod() {
		return freePeriod;
	}
	public void setFreePeriod(int[][] freePeriod) {
		this.freePeriod = freePeriod;
	}
	public double getConsecutiveClassTime() {
		return consecutiveClassTime;
	}
	public void setConsecutiveClassTime(double consecutiveClassTime) {
		this.consecutiveClassTime = consecutiveClassTime;
	}
	public double getIntervalTime() {
		return intervalTime;
	}
	public void setIntervalTime(double intervalTime) {
		this.intervalTime = intervalTime;
	}
	
	
}
